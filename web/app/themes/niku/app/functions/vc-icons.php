/**
 * Override vc icons
 *
 * @return void
 * @author tokoo
 **/

add_action( 'vc_base_register_front_css', 'tokoo_register_custom_font_front_vc' );
add_action( 'vc_base_register_admin_css', 'tokoo_register_custom_font_front_vc' );
add_action( 'vc_backend_editor_enqueue_js_css', 'tokoo_enqueue_custom_fonts_vc' );
add_action( 'vc_frontend_editor_enqueue_js_css', 'tokoo_enqueue_custom_fonts_vc' );
add_action( 'wp_enqueue_scripts', 'tokoo_enqueue_custom_fonts_vc' );
add_filter( 'vc_iconpicker-type-ionicons', 'tokoo_ionic_icon_list' );
add_filter( 'vc_iconpicker-type-themifyicons', 'tokoo_themify_icon_list' );

/**
 * Register custom font vc
 *
 * @return void
 * @author tokoo
 **/
function tokoo_register_custom_font_front_vc() {
	wp_register_style( 'vc_ionicons', TOKOO_THEME_ASSETS_URI . '/css/ionicons.min.css', false, '', 'screen' );
	wp_register_style( 'vc_themifyicons', TOKOO_THEME_ASSETS_URI . '/css/themify-icons.css', false, '', 'screen' );
}

/**
 * Enqueue custom font vc
 *
 * @return void
 * @author tokoo
 **/
function tokoo_enqueue_custom_fonts_vc() {
	wp_enqueue_style( 'vc_ionicons' );
	wp_enqueue_style( 'vc_themifyicons' );
}

